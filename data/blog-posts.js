// Blog post helper fonksiyonları
export function getAllBlogPosts() {
  return blogPosts.sort(
    (a, b) => new Date(b.publishedAt) - new Date(a.publishedAt)
  );
}

export function getBlogPostBySlug(slug) {
  return blogPosts.find((post) => post.slug === slug);
}

export function getBlogPostsByCategory(categorySlug) {
  if (categorySlug === "all") {
    return getAllBlogPosts();
  }
  return blogPosts
    .filter((post) => post.categorySlug === categorySlug)
    .sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt));
}

export function getFeaturedBlogPosts() {
  return blogPosts
    .filter((post) => post.featured)
    .sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt));
}

export function getRelatedBlogPosts(currentPostId, categorySlug, limit = 3) {
  return blogPosts
    .filter(
      (post) => post.id !== currentPostId && post.categorySlug === categorySlug
    )
    .sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt))
    .slice(0, limit);
}

export function searchBlogPosts(searchTerm) {
  const term = searchTerm.toLowerCase();
  return blogPosts
    .filter(
      (post) =>
        post.title.toLowerCase().includes(term) ||
        post.excerpt.toLowerCase().includes(term) ||
        post.content.toLowerCase().includes(term) ||
        post.tags.some((tag) => tag.toLowerCase().includes(term))
    )
    .sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt));
}
